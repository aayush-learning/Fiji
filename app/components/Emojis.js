function emojiStringToArray(str) {
  // split = str.split(/([\uD800-\uDBFF][\uDC00-\uDFFF])/);
  split = str.split(' ')
  arr = [];
  for (var i=0; i<split.length; i++) {
    char = split[i]
    if (char !== "" && char !== ' ') {
      arr.push(char);
    }
  }
  return arr;
};
